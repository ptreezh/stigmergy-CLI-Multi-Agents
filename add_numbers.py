def add_numbers(a, b):
    """
    Add two numbers together.
    
    Args:
        a: First number (int or float)
        b: Second number (int or float)
    
    Returns:
        The sum of a and b
    """
    return a + b

# Example usage
if __name__ == "__main__":
    result = add_numbers(5, 3)
    print(f"The sum is: {result}")